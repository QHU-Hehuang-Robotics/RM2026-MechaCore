//motor data read
#define get_motor_measure(ptr, data)                                    \
    {                                                                   \
        (ptr)->last_ecd = (ptr)->ecd;                                   \
        (ptr)->ecd = (uint16_t)((data)[0] << 8 | (data)[1]);            \
        (ptr)->speed_rpm = (uint16_t)((data)[2] << 8 | (data)[3]);      \
        (ptr)->given_current = (uint16_t)((data)[4] << 8 | (data)[5]);  \
        (ptr)->temperate = (data)[6];                                   \
    }
/*
motor data,  0:chassis motor1 3508;1:chassis motor3 3508;2:chassis motor3 3508;3:chassis motor4 3508;
4:yaw gimbal motor 6020;5:pitch gimbal motor 6020;6:trigger motor 2006;
电机数据, 0:底盘电机1 3508电机,  1:底盘电机2 3508电机,2:底盘电机3 3508电机,3:底盘电机4 3508电机;
4:yaw云台电机 6020电机; 5:pitch云台电机 6020电机; 6:拨弹电机 2006电机*/
static motor_measure_t motor_chassis[7];
static dm_motor_measure_t dm_motor_measure[1];

static CAN_TxHeaderTypeDef  gimbal_tx_message;
static uint8_t              gimbal_can_send_data[8];
static CAN_TxHeaderTypeDef  chassis_tx_message;
static uint8_t              chassis_can_send_data[8];
static CAN_TxHeaderTypeDef  DMMIT_tx_message;
static uint8_t              DMMIT_can_send_data[8];


/**
  * @brief          hal CAN fifo call back, receive motor data
  * @param[in]      hcan, the point to CAN handle
  * @retval         none
  */
/**
  * @brief          hal库CAN回调函数,接收电机数据
  * @param[in]      hcan:CAN句柄指针
  * @retval         none
  */
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
    CAN_RxHeaderTypeDef rx_header;
    uint8_t rx_data[8];

    HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &rx_header, rx_data);

    switch (rx_header.StdId)
    {
        case CAN_3508_M1_ID:
        case CAN_3508_M2_ID:
        case CAN_3508_M3_ID:
        case CAN_3508_M4_ID:
        case CAN_YAW_MOTOR_ID:
        case CAN_PIT_MOTOR_ID:
        case CAN_TRIGGER_MOTOR_ID:
        {
            static uint8_t i = 0;
            //get motor id
            i = rx_header.StdId - CAN_3508_M1_ID;
            get_motor_measure(&motor_chassis[i], rx_data);
            detect_hook(CHASSIS_MOTOR1_TOE + i);
            break;
        }

          case CAN_DM4340_M1_FEEDBACK_ID : 
        {
            decode_dm_motor(dm_motor_measure,rx_data);
            break;
        }


        default:
        {
            break;
        }
    }
}


void decode_dm_motor(dm_motor_measure_t *ptr, uint8_t *rx_data)
{
    // 1. 提取原始整数
    uint16_t p_int = (rx_data[1] << 8) | rx_data[2];
    uint16_t v_int = (rx_data[3] << 4) | (rx_data[4] >> 4);
    uint16_t t_int = ((rx_data[4] & 0xF) << 8) | rx_data[5];

    // 2. 转换为物理量
    ptr->pos = uint_to_float(p_int, -12.5f, 12.5f, 16);
    ptr->vel = uint_to_float(v_int, -45.0f, 45.0f, 12);
    ptr->torque = uint_to_float(t_int, -18.0f, 18.0f, 12);

    // 3. 相对零位处理
    if (ptr->first_flag == 0) {
        ptr->offset_pos = ptr->pos; // 记录上电瞬间的绝对位置
        ptr->first_flag = 1;        // 标记已初始化
    }
    ptr->relative_pos = ptr->pos - ptr->offset_pos; // 计算相对角度

    // 4. 更新状态
    ptr->state = rx_data[0] >> 4;
    ptr->temp_mos = rx_data[6];
    ptr->temp_rotor = rx_data[7];
    ptr->last_update_tick = HAL_GetTick();
}








/**
  * @brief          send control current of motor (0x205, 0x206, 0x207, 0x208)
  * @param[in]      yaw: (0x205) 6020 motor control current, range [-30000,30000] 
  * @param[in]      pitch: (0x206) 6020 motor control current, range [-30000,30000]
  * @param[in]      shoot: (0x207) 2006 motor control current, range [-10000,10000]
  * @param[in]      rev: (0x208) reserve motor control current
  * @retval         none
  */
/**
  * @brief          发送电机控制电流(0x205,0x206,0x207,0x208)
  * @param[in]      yaw: (0x205) 6020电机控制电流, 范围 [-30000,30000]
  * @param[in]      pitch: (0x206) 6020电机控制电流, 范围 [-30000,30000]
  * @param[in]      shoot: (0x207) 2006电机控制电流, 范围 [-10000,10000]
  * @param[in]      rev: (0x208) 保留，电机控制电流
  * @retval         none
  */
void CAN_cmd_gimbal(int16_t yaw, int16_t pitch, int16_t shoot, int16_t rev)
{
    uint32_t send_mail_box;
    gimbal_tx_message.StdId = CAN_GIMBAL_ALL_ID;
    gimbal_tx_message.IDE = CAN_ID_STD;
    gimbal_tx_message.RTR = CAN_RTR_DATA;
    gimbal_tx_message.DLC = 0x08;
    gimbal_can_send_data[0] = (yaw >> 8);
    gimbal_can_send_data[1] = yaw;
    gimbal_can_send_data[2] = (pitch >> 8);
    gimbal_can_send_data[3] = pitch;
    gimbal_can_send_data[4] = (shoot >> 8);
    gimbal_can_send_data[5] = shoot;
    gimbal_can_send_data[6] = (rev >> 8);
    gimbal_can_send_data[7] = rev;
    HAL_CAN_AddTxMessage(&GIMBAL_CAN, &gimbal_tx_message, gimbal_can_send_data, &send_mail_box);
}


int float_to_uint(float x, float x_min, float x_max, int bits) 
{
    float span = x_max - x_min;
    float offset = x_min;
    return (int)((x - offset) * ((float)((1 << bits) - 1)) / span);
}

float uint_to_float(int x_int, float x_min, float x_max, int bits) 
{
    float span = x_max - x_min;
    float offset = x_min;
    return ((float)x_int) * span / (float)((1 << bits) - 1) + offset;
}

void CAN_DM_MIT(float p_des, float v_des, float kp, float kd, float t_ff)
{
  uint32_t dm_mailbox;

  DMMIT_tx_message.StdId = CAN_DM4340_M1_ID;
  DMMIT_tx_message.IDE   = CAN_ID_STD;
  DMMIT_tx_message.RTR   = CAN_RTR_DATA;
  DMMIT_tx_message.DLC   = 0x08;
  
  uint16_t p_int = float_to_uint(p_des, -12.5f, 12.5f, 16);
  uint16_t v_int = float_to_uint(v_des, -45.0f, 45.0f, 12);
  uint16_t kp_int = float_to_uint(kp, 0.0f, 500.0f, 12);
  uint16_t kd_int = float_to_uint(kd, 0.0f, 5.0f, 12);
  uint16_t t_int = float_to_uint(t_ff, -18.0f, 18.0f, 12);
  DMMIT_can_send_data[0] = p_int >> 8;                      // POS[15:8]
  DMMIT_can_send_data[1] = p_int & 0xFF;                    // POS[7:0]
  DMMIT_can_send_data[2] = v_int >> 4;                      // VEL[11:4]
  DMMIT_can_send_data[3] = ((v_int & 0xF) << 4) | (kp_int >> 8); // VEL[3:0] | KP[11:8]
  DMMIT_can_send_data[4] = kp_int & 0xFF;                   // KP[7:0]
  DMMIT_can_send_data[5] = kd_int >> 4;                      // KD[11:4]
  DMMIT_can_send_data[6] = ((kd_int & 0xF) << 4) | (t_int >> 8); // KD[3:0] | T[11:8]
  DMMIT_can_send_data[7] = t_int & 0xFF;                    // T[7:0]

  HAL_CAN_AddTxMessage(&GIMBAL_CAN, &DMMIT_tx_message, DMMIT_can_send_data, &dm_mailbox);

}














/**
  * @brief          send CAN packet of ID 0x700, it will set chassis motor 3508 to quick ID setting
  * @param[in]      none
  * @retval         none
  */
/**
  * @brief          发送ID为0x700的CAN包,它会设置3508电机进入快速设置ID
  * @param[in]      none
  * @retval         none
  */
void CAN_cmd_chassis_reset_ID(void)
{
    uint32_t send_mail_box;
    chassis_tx_message.StdId = 0x700;
    chassis_tx_message.IDE = CAN_ID_STD;
    chassis_tx_message.RTR = CAN_RTR_DATA;
    chassis_tx_message.DLC = 0x08;
    chassis_can_send_data[0] = 0;
    chassis_can_send_data[1] = 0;
    chassis_can_send_data[2] = 0;
    chassis_can_send_data[3] = 0;
    chassis_can_send_data[4] = 0;
    chassis_can_send_data[5] = 0;
    chassis_can_send_data[6] = 0;
    chassis_can_send_data[7] = 0;

    HAL_CAN_AddTxMessage(&CHASSIS_CAN, &chassis_tx_message, chassis_can_send_data, &send_mail_box);
}


